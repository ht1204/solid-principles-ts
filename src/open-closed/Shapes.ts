export class Triangle {
    public base: number;
    public height: number;
    constructor(base: number, height: number) {
        this.base = base;
        this.height = height;
    }
}


export class Rectangle {
    public width: number;
    public height: number;
    constructor(width: number, height: number) {
        this.width = width;
        this.height = height;
    }
}

export class Circle {
    public radius: number;
    constructor(radius: number) {
        this.radius = radius;
    }
}



